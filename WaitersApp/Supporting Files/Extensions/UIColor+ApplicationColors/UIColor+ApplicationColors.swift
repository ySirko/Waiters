import UIKit

extension UIColor {
   
    class func redApplicationColor() -> UIColor {
        return UIColor(red:0.86, green:0.27, blue:0.21, alpha:1)
    }
    
    class func darkGrayApplicationColor() -> UIColor {
        return UIColor(red:0.17, green:0.17, blue:0.17, alpha:1)
    }
    
    class func lightBlueApplicationColor() -> UIColor {
        return UIColor(red:0.63, green:0.81, blue:0.8, alpha:1)
    }
}